

-- 댓글 테이블
CREATE TABLE FREEBOARD_REPLY(
	freeboard_replynum NUMBER PRIMARY KEY
	, freeboard_num NUMBER NOT NULL REFERENCES EVERYDAYGYM_FREEBOARD(freeboard_num) ON DELETE CASCADE
	, user_id VARCHAR2(50) NOT NULL
	, freeboard_replytext VARCHAR2(1000) NOT NULL
	, freeboard_reply_indate DATE DEFAULT SYSDATE
);

CREATE SEQUENCE FREEBOARD_REPLY_SEQ;
